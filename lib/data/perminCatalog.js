/**
 * Look up a yarn by EAN barcode or Permin article number.
 *
 * Confirmed: Permin's GS1 company prefix is 570285
 * EAN-13 format: 570285 + articleNumber(6) + checkDigit
 * Example: 883174 (Bella Color Blå/Beige/Brun) → EAN 5702858831748
 *
 * For newer 7-digit article numbers (8832100–8832105), prefix is 57028 (5 digits).
 */
export function lookupByBarcode(scannedCode) {
  if (!scannedCode) return null
  const code = String(scannedCode).trim()

  // Direct article number match (user typed it manually)
  const direct = PERMIN_CATALOG.find(e => e.articleNumber === code)
  if (direct) return direct

  // EAN-13 with Permin GS1 prefix 570285 — 6-digit article number
  if (code.length === 13 && code.startsWith('570285')) {
    const article = code.slice(6, 12)
    const match = PERMIN_CATALOG.find(e => e.articleNumber === article)
    if (match) return match
  }

  // EAN-13 with 5-digit prefix 57028 — 7-digit article number (newer Bella series)
  if (code.length === 13 && code.startsWith('57028')) {
    const article7 = code.slice(5, 12)
    const match7 = PERMIN_CATALOG.find(e => e.articleNumber === article7)
    if (match7) return match7
  }

  return null
}
